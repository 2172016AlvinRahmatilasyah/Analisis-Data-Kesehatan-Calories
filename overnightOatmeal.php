    <section class="recipe">
        <img src="assets/overnightoeatmeal.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>1/2 cup rolled oats</li>
            <li>1/2 cup unsweetened almond milk (or any milk of your choice)</li>
            <li>1/4 cup non-fat Greek yogurt</li>
            <li>1 tablespoon chia seeds (optional)</li>
            <li>1/2 teaspoon vanilla extract</li>
            <li>1 tablespoon honey or maple syrup (optional)</li>
            <li>Fresh fruit for topping (such as berries, banana slices, or diced apples)</li>
            <li>Nuts or seeds for topping (such as almonds, walnuts, or pumpkin seeds)</li>
            <li>Cinnamon or nutmeg for flavor (optional)</li>
        </ul>
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li>Combine Ingredients:</li>
            <li>In a mason jar or airtight container, combine the rolled oats, almond milk, Greek yogurt, chia seeds (if using), vanilla extract, and honey or maple syrup (if using).</li>
            <li>Stir well to combine all ingredients evenly.</li>
            <br><br>
            <li>Refrigerate Overnight:</li>
            <li>Cover the jar or container and refrigerate the oatmeal mixture overnight, or for at least 4 hours, to allow the oats to soften and absorb the liquid.</li>
            <br><br>
            <li>Serve:</li>
            <li>The next morning, remove the oatmeal from the refrigerator and give it a good stir.</li>
            <li>If desired, add more milk to adjust the consistency.</li>
            <li>Top the oatmeal with fresh fruit, nuts, seeds, and a sprinkle of cinnamon or nutmeg for extra flavor.</li>
            <br><br>
            <li>Enjoy:</li>
            <li>Enjoy your Healthy Low-Calorie Overnight Oatmeal cold straight from the refrigerator, or you can warm it up in the microwave for a cozy breakfast option.</li>
            <li>Slice the sandwich in half diagonally or enjoy it as a whole.</li>
            <li>Serve immediately and enjoy your Healthy Low-Calorie Sandwich!</li>
            <br><br>
            <l1><strong>Note:</strong></l1>
            <li><strong>This Healthy Low-Calorie Overnight Oatmeal is not only delicious and satisfying but also a convenient option for busy mornings. Plus, it's packed with fiber, protein, and nutrients to keep you fueled throughout the day!</strong></li>
            <li><strong>Experiment with different toppings and flavorings, such as cocoa powder, shredded coconut, or peanut butter, to create your favorite combinations.</strong></li>
            <li><strong>You can also add protein powder or nut butter to increase the protein content of your overnight oats.</strong></li>
        </ol>
