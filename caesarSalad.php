    <section class="recipe">
        <img src="assets/caesarSalad.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>1/4 cup non-fat Greek yogurt</li>
            <li>1 tablespoon fat-free mayonnaise</li>
            <li>1 tablespoon fresh lemon juice</li>
            <li>1 teaspoon Dijon mustard</li>
            <li>1 teaspoon Worcestershire sauce</li>
            <li>1 clove garlic, finely chopped</li>
            <li>Salt and black pepper to taste</li>
            <li>1 head romaine lettuce, chopped</li>
            <li>1/2 cup cherry tomatoes, halved</li>
            <li>1/4 cup low-fat Parmesan cheese, grated</li>
            <li>1 stalk celery, thinly sliced</li>
            <li>2 grilled skinless chicken breasts, diced (optional)</li>
        </ul>
        
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li><strong>Make Dressing:</strong> Mix Greek yogurt, fat-free mayonnaise, lemon juice, Dijon mustard,
                Worcestershire sauce, and chopped garlic in a bowl. Stir until well combined. Season with salt and black
                pepper to taste.</li>
            <li><strong>Prepare Salad:</strong> In a large bowl, combine chopped romaine lettuce, cherry tomatoes,
                grated low-fat Parmesan cheese, and sliced celery. If using, add diced grilled chicken to the bowl.</li>
            <li><strong>Serve:</strong> Pour the dressing evenly over the salad and toss until all ingredients are
                well-coated. Serve immediately.</li>
        </ol>
    </section>

