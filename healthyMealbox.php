            <h2>Grilled Chicken Breast:</h2>
            <p>Ingredients:</p>
            <ul>
                <li>2 boneless, skinless chicken breasts</li>
                <li>1 tablespoon olive oil</li>
                <li>1 teaspoon garlic powder</li>
                <li>1 teaspoon paprika</li>
                <li>Salt and pepper to taste</li>
            </ul>
            <p>Instructions:</p>
            <ol>
                <li>Preheat the grill or grill pan over medium-high heat.</li>
                <li>Rub chicken breasts with olive oil, garlic powder, paprika, salt, and pepper.</li>
                <li>Grill the chicken for 6-7 minutes per side or until fully cooked and juices run clear.</li>
            </ol>
        </div>
