    <section class="recipe">
        <div class="juice">
            <img src="assets/quinoasalad.jpg">
            <h2>Quinoa</h2>
            <p>Ingredients:</p>
            <ul>
                <li>1 cup quinoa, rinsed</li>
                <li>2 cups vegetable broth</li>
                <li>1/2 teaspoon salt</li>
            </ul>
            <p>Instructions:</p>
            <ol>
                <li>In a saucepan, combine quinoa, vegetable broth, and salt.</li>
                <li>Bring to a boil, then reduce heat to low, cover, and simmer for 15-20 minutes, or until quinoa is cooked and liquid is absorbed.</li>
                <li>Fluff quinoa with a fork.</li>
            </ol>
        </div>
    </section>

    <section class="recipe">
        <div class="juice">
            <img src="assets/RoastedVegetables.jpg">
            <h2>Roasted Vegetables</h2>
            <p>Ingredients:</p>
            <ul>
                <li>2 cups mixed vegetables (bell peppers, zucchini, cherry tomatoes, etc.), chopped</li>
                <li>1 tablespoon olive oil</li>
                <li>1 teaspoon dried thyme</li>
                <l1>Salt and pepper to taste</l1>
            </ul>
            <p>Instructions:</p>
            <ol>
                <li>Preheat the oven to 400°F (200°C).</li>
                <li>Toss chopped vegetables with olive oil, dried thyme, salt, and pepper.</li>
                <li>Spread vegetables on a baking sheet and roast for 20-25 minutes, or until they are tender and slightly browned.</li>
            </ol>
        </div>
    </section>

