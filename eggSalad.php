    <section class="recipe">
        <img src="assets/eggSalad.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>4 hard-boiled eggs, peeled and chopped</li>
            <li>1/4 cup plain Greek yogurt</li>
            <li>1 tablespoon Dijon mustard</li>
            <li>1 tablespoon lemon juice</li>
            <li>2 green onions, thinly sliced</li>
            <li>1 celery stalk, finely chopped</li>
            <li>1 tablespoon fresh dill, chopped (optional)</li>
            <li>Salt and pepper to taste</li>
            <li>Lettuce leaves for serving (optional)</li>
        </ul>
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li>Prepare the Hard-Boiled Eggs:</li>
            <li>Place eggs in a saucepan and cover with cold water.</li>
            <li>Bring the water to a boil, then cover and remove from heat.</li>
            <li>Let the eggs sit in the hot water for 9-12 minutes, depending on desired doneness.</li>
            <li>Transfer the eggs to a bowl of ice water to cool completely, then peel and chop them.</li>
            <br><br>
            <li>Make the Egg Salad:</li>
            <li>In a mixing bowl, combine the chopped hard-boiled eggs, plain Greek yogurt, Dijon mustard, lemon juice, sliced green onions, chopped celery, and fresh dill (if using).</li>
            <li>Mix until all ingredients are well combined and the egg salad is creamy.</li>
            <li>Season with salt and pepper to taste.</li>
            <br><br>
            <li>Serve:</li>
            <li>Serve the Healthy Low-Calorie Egg Salad on its own, or use it as a filling for sandwiches or wraps.</li>
            <li>For a lighter option, serve the egg salad over a bed of lettuce leaves.</li>
            <br><br>
            <li>Storage:</li>
            <li>Store any leftover egg salad in an airtight container in the refrigerator for up to 3 days.</li>
            <li>Slice the sandwich in half diagonally or enjoy it as a whole.</li>
            <li>Serve immediately and enjoy your Healthy Low-Calorie Sandwich!</li>
            
        </ol>
    </section>

