    <section class="recipe">
        <img src="assets/healthySandwich.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>2 slices whole grain bread</li>
            <li>2 ounces cooked chicken breast, thinly sliced</li>
            <li>1/4 avocado, mashed</li>
            <li>1/4 cup mixed salad greens (such as spinach, arugula, or lettuce)</li>
            <li>2 slices tomato</li>
            <li>2 slices cucumber</li>
            <li>1 tablespoon hummus (optional)</li>
            <li>1 teaspoon mustard (optional)</li>
            <li>Salt and pepper to taste</li>
        </ul>
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li>Prepare the Bread:</li>
            <li>Toast the whole grain bread slices until lightly golden brown.</li>
            <br><br>
            <li>Assemble the Sandwich:</li>
            <li>Spread the mashed avocado evenly over one slice of toasted bread.</li>
            <li>Layer the sliced chicken breast on top of the mashed avocado.</li>
            <li>Add the mixed salad greens, tomato slices, and cucumber slices on top of the chicken.</li>
            <br><br>
            <li>Add Optional Ingredients:</li>
            <li>If desired, spread hummus on the other slice of toasted bread and place it on top of the sandwich.</li>
            <li>Alternatively, spread mustard on the other slice of toasted bread before assembling the sandwich.</li>
            <br><br>
            <li>Season and Serve:</li>
            <li>Season the sandwich with salt and pepper to taste.</li>
            <li>Slice the sandwich in half diagonally or enjoy it as a whole.</li>
            <li>Serve immediately and enjoy your Healthy Low-Calorie Sandwich!</li>
            <br><br>
            <li><strong>Note: Feel free to customize this sandwich by adding your favorite vegetables or swapping the chicken for lean turkey or tofu for a vegetarian option. You can also skip the hummus and mustard if you prefer a simpler flavor profile. This sandwich is perfect for a quick and satisfying low-calorie meal!</strong></li>
        </ol>
    </section>

