    <section class="recipe">
        <img src="assets/vegetablesPeanutSauce.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>For the Vegetable Mix:</li>
            <li>2 cups mixed vegetables (such as broccoli florets, bell peppers, carrots, and snap peas), chopped</li>
            <li>1 tablespoon olive oil</li>
            <li>Salt and pepper to taste</li>
            <br><br>
            <li>For the Peanut Sauce:</li>
            <li>2 tablespoons natural peanut butter</li>
            <li>1 tablespoon low-sodium soy sauce</li>
            <li>1 teaspoon sesame oil</li>
            <li>1 teaspoon honey or maple syrup</li>
            <li>1/2 teaspoon grated fresh ginger</li>
            <li>1 clove garlic, minced</li>
            <li>2-4 tablespoons water (adjust to desired consistency)</li>
        </ul>
        
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li>Prepare the Vegetable Mix:</li>
            <li>Heat olive oil in a large skillet over medium heat.</li>
            <li>Add the chopped mixed vegetables to the skillet and sauté until they are tender yet still crisp, about 5-7 minutes.</li>
            <li>Season with salt and pepper to taste and set aside.</li>
            <br><br>
            <li>Make the Peanut Sauce:</li>
            <li>In a small bowl, whisk together peanut butter, low-sodium soy sauce, rice vinegar, sesame oil, honey or maple syrup, grated ginger, and minced garlic until well combined.</li>
            <li>Gradually add water, 1 tablespoon at a time, until the sauce reaches your desired consistency. The sauce should be smooth and pourable.</li>
            <br><br>
            <li>Combine and Serve:</li>
            <li>Pour the prepared peanut sauce over the sautéed mixed vegetables and toss gently until all vegetables are coated evenly with the sauce.</li>
            <li>Serve the Mix Vegetable and Peanut Sauce warm as a side dish or over cooked quinoa or brown rice for a complete meal.</li>
        </ol>
