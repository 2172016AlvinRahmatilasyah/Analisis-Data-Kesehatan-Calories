    <section class="recipe">
        <img src="assets/fruitsalad.jpg">
        <h2>Ingredients:</h2>
        <ul>
            <li>2 cups strawberries, halved</li>
            <li>1 ripe mango, peeled and diced</li>
            <li>1 honeydew melon, peeled and diced</li>
            <li>1 green grape bunch, cut into several pieces</li>
            <li>1 kiwi, peeled and diced</li>
            <li>1 mandarin orange, segmented</li>
            <li>1 tablespoon honey (optional)</li>
            <li>1 teaspoon fresh lemon juice</li>
            <li>Fresh mint leaves for garnish (optional)</li>
        </ul>
    </section>

    <section class="instructions">
        <h2>Instructions:</h2>
        <ol>
            <li>Mix all the fruits in a large bowl.</li>
            <li>If using honey, combine honey with fresh lemon juice and stir until well mixed.</li>
            <li>Pour the honey mixture over the fruit bowl and gently toss until all fruits are evenly coated with honey.</li>
            <li>Serve in a serving bowl and garnish with fresh mint leaves if desired.</li>
            <li>Enjoy this low-calorie Fruit Salad as a healthy snack or a light dessert!</li>
            <br><br>
            <l1><strong>Note: You can adjust the types of fruits according to your personal preferences. If you want to add other variations such as red grapes or pineapple chunks, those can also be good choices. Make sure to choose fresh and ripe fruits for the best results.</strong></l1>
        </ol>
